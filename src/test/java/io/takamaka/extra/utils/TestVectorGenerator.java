    private static void generateAesCbcVector(String password, String content, String scope, byte[] iv) throws Exception {
        // Derive key using PBKDF2
        PBEKeySpec spec = new PBEKeySpec(
            password.toCharArray(),
            scope.getBytes(StandardCharsets.UTF_8),
            20000,
            256
        );
        SecretKeyFactory skf = SecretKeyFactory.getInstance("PBKDF2WithHmacSHA512");
        byte[] key = skf.generateSecret(spec).getEncoded();
        SecretKeySpec keySpec = new SecretKeySpec(key, "AES");

        // Encrypt with AES-CBC
        Cipher cipher = Cipher.getInstance("AES/CBC/PKCS5Padding");
        cipher.init(Cipher.ENCRYPT_MODE, keySpec, new IvParameterSpec(iv));
        byte[] ciphertext = cipher.doFinal(content.getBytes(StandardCharsets.UTF_8));

        System.out.println("AES-CBC Vector:");
        System.out.println("  password: \"" + password + "\"");
        System.out.println("  content: \"" + content + "\"");
        System.out.println("  scope: \"" + scope + "\"");
        System.out.println("  iv_hex: \"" + bytesToHex(iv) + "\"");
        System.out.println("  iv_b64url: \"" + TkmSignUtils.fromByteArrayToB64URL(iv) + "\"");
        System.out.println("  ciphertext_hex: \"" + bytesToHex(ciphertext) + "\"");
        System.out.println("  ciphertext_b64url: \"" + TkmSignUtils.fromByteArrayToB64URL(ciphertext) + "\"");
        System.out.println("  derivedKey_hex: \"" + bytesToHex(key) + "\"");
        System.out.println();
    }

