    /**
     * Generate PBKDF2-HMAC-SHA512 key derivation test vectors.
     */
    private static void generatePBKDF2Vectors() throws Exception {
        System.out.println("--- PBKDF2-HMAC-SHA512 Test Vectors ---");

        String[][] testCases = {
            {"password", "salt", "20000", "256"},
            {"test-password-12345", "test-scope", "20000", "256"},
            {"complex!@#$%^&*()password", "unicode-scope-日本語", "20000", "256"},
            {"symmetric-key-400chars-abcdefghijklmnopqrstuvwxyz", "conversation-salt-32chars", "20000", "256"},
        };

        for (String[] tc : testCases) {
            String password = tc[0];
            String salt = tc[1];
            int iterations = Integer.parseInt(tc[2]);
            int keyLengthBits = Integer.parseInt(tc[3]);

            PBEKeySpec spec = new PBEKeySpec(
                password.toCharArray(),
                salt.getBytes(StandardCharsets.UTF_8),
                iterations,
                keyLengthBits
            );
            SecretKeyFactory skf = SecretKeyFactory.getInstance("PBKDF2WithHmacSHA512");
            byte[] key = skf.generateSecret(spec).getEncoded();

            System.out.println("PBKDF2 Vector:");
            System.out.println("  password: \"" + password + "\"");
            System.out.println("  salt: \"" + salt + "\"");
            System.out.println("  iterations: " + iterations);
            System.out.println("  keyLengthBits: " + keyLengthBits);
            System.out.println("  derivedKey_hex: \"" + bytesToHex(key) + "\"");
            System.out.println("  derivedKey_b64url: \"" + TkmSignUtils.fromByteArrayToB64URL(key) + "\"");
            System.out.println();
        }
    }

    /**
     * Generate SHA3-256 hash test vectors.
     */
    private static void generateSHA3256Vectors() throws Exception {
        System.out.println("--- SHA3-256 Test Vectors ---");

        String[] inputs = {
            "Hello, World!",
            "Test input string",
            "My Conversation",
            "unicode-日本語-\uD83D\uDE00",
            "The quick brown fox jumps over the lazy dog",
        };

        for (String input : inputs) {
            SHA3Digest digest = new SHA3Digest(256);
            byte[] inputBytes = input.getBytes(StandardCharsets.UTF_8);
            digest.update(inputBytes, 0, inputBytes.length);
            byte[] hash = new byte[digest.getDigestSize()];
            digest.doFinal(hash, 0);

            System.out.println("SHA3-256 Vector:");
            System.out.println("  input: \"" + input + "\"");
            System.out.println("  hash_hex: \"" + bytesToHex(hash) + "\"");
            System.out.println("  hash_b64url: \"" + TkmSignUtils.fromByteArrayToB64URL(hash) + "\"");
            System.out.println();
        }
    }

